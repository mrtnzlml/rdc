//! `_index.md` generator for a pulled env snapshot.
//!
//! The file is regenerated on every `rdc pull` and at the end of every
//! `rdc push`. It's the entry point for an agent (Claude Code or
//! similar) or a human dropping into the repo: every object is listed
//! with its on-disk path, human name, type-specific signals, and the
//! related objects it points at (and that point at it).
//!
//! Design goals:
//! - **Deterministic.** Same lockfile + same on-disk JSONs ⇒ byte-for-byte
//!   identical output. No timestamps in the body.
//! - **Best-effort.** Missing or unparseable JSON files are silently
//!   skipped; a fresh pull is what fully populates the index.
//! - **Paths first.** Every entry has a `path:` line so an agent can jump
//!   straight from the index to the source file without learning the
//!   layout convention.

use crate::paths::Paths;
use crate::state::Lockfile;
use anyhow::{Context, Result};
use serde_json::Value;
use std::collections::BTreeMap;
use std::path::{Path, PathBuf};

/// Generate `<env_root>/_index.md` from the lockfile + on-disk JSONs.
pub fn generate(paths: &Paths, lockfile: &Lockfile) -> Result<()> {
    let env_root = paths.env_root();
    if !env_root.exists() {
        return Ok(());
    }

    let ctx = IndexCtx::build(paths, lockfile);

    let mut md = String::new();
    md.push_str("<!-- generated by rdc; do not edit -->\n");
    md.push_str(&format!("# {}\n\n", paths.env()));
    md.push_str(
        "This file is regenerated on every `rdc pull` and `rdc push`. \
         Edit the snapshot files (paths listed below), not this index.\n\n",
    );
    // Cross-link to the project-root agent guide if it exists. The
    // guide is created once by `rdc init`; users on older projects may
    // not have one. The link is relative because _index.md sits two
    // levels below the root.
    if paths.root().join("CLAUDE.md").exists() {
        md.push_str(
            "Editing recipes, layout, and workflow notes live in \
             [`../../CLAUDE.md`](../../CLAUDE.md).\n\n",
        );
    }

    emit_inventory(&mut md, lockfile);

    // Per-kind rich sections, in dependency order so a reader builds
    // context from the outside in.
    emit_organization(&mut md, &ctx);
    emit_workspaces(&mut md, &ctx);
    emit_queues(&mut md, &ctx);
    emit_schemas(&mut md, &ctx);
    emit_inboxes(&mut md, &ctx);
    emit_hooks(&mut md, &ctx);
    emit_rules(&mut md, &ctx);
    emit_labels(&mut md, &ctx);
    emit_engines(&mut md, &ctx);
    emit_engine_fields(&mut md, &ctx);
    emit_workflows(&mut md, &ctx);
    emit_workflow_steps(&mut md, &ctx);
    emit_email_templates(&mut md, &ctx);

    // Any other kinds we haven't modeled (mdh_*, future) get a terse
    // catch-all so they're at least listed.
    emit_unknown_kinds(&mut md, lockfile);

    let path = env_root.join("_index.md");
    crate::snapshot::writer::write_atomic(&path, md.as_bytes())
        .with_context(|| format!("writing {}", path.display()))?;

    Ok(())
}

/// Kinds the rich emitters cover. Used to filter the catch-all section.
const RICH_KINDS: &[&str] = &[
    "organization",
    "workspaces",
    "queues",
    "schemas",
    "inboxes",
    "hooks",
    "rules",
    "labels",
    "engines",
    "engine_fields",
    "workflows",
    "workflow_steps",
    "email_templates",
];

// ----- Context with reverse indices -----------------------------------

/// Pre-computed reverse cross-reference maps. Built once per index
/// generation by walking the writable kinds' JSON files. All maps key
/// off slugs so the per-kind emitters can look up "what points at this"
/// in O(log n).
struct IndexCtx<'a> {
    paths: &'a Paths,
    lockfile: &'a Lockfile,
    /// queue_slug → workspace_slug (resolved by walking the disk layout)
    queue_to_ws_slug: BTreeMap<String, String>,
    /// queue_slug → [hook_slug, ...]
    queue_to_hooks: BTreeMap<String, Vec<String>>,
    /// queue_slug → [rule_slug, ...]
    queue_to_rules: BTreeMap<String, Vec<String>>,
    /// queue_slug → [<ws>/<q>/<t> compound key, ...]
    queue_to_email_templates: BTreeMap<String, Vec<String>>,
    /// engine_slug → [engine_field_slug, ...]
    engine_to_fields: BTreeMap<String, Vec<String>>,
}

impl<'a> IndexCtx<'a> {
    fn build(paths: &'a Paths, lockfile: &'a Lockfile) -> Self {
        let queue_to_ws_slug = build_queue_to_ws(paths);
        let queue_to_hooks = build_queue_back_refs(paths.hooks_dir(), lockfile, "queues");
        let queue_to_rules = build_queue_back_refs(paths.rules_dir(), lockfile, "queues");
        let queue_to_email_templates = build_queue_to_email_templates(lockfile);
        let engine_to_fields = build_engine_to_fields(paths, lockfile);
        Self {
            paths,
            lockfile,
            queue_to_ws_slug,
            queue_to_hooks,
            queue_to_rules,
            queue_to_email_templates,
            engine_to_fields,
        }
    }
}

/// Walk `workspaces/<ws>/queues/<q>/` to learn each queue's parent
/// workspace. The disk layout is authoritative here — works even when
/// queue.json is missing or unparseable.
fn build_queue_to_ws(paths: &Paths) -> BTreeMap<String, String> {
    let mut out = BTreeMap::new();
    let ws_root = paths.workspaces_dir();
    let Ok(ws_entries) = std::fs::read_dir(&ws_root) else { return out };
    for ws in ws_entries.flatten() {
        let Ok(ft) = ws.file_type() else { continue };
        if !ft.is_dir() { continue }
        let ws_slug = ws.file_name().to_string_lossy().to_string();
        let queues_dir = paths.queues_dir(&ws_slug);
        let Ok(q_entries) = std::fs::read_dir(&queues_dir) else { continue };
        for q in q_entries.flatten() {
            let Ok(ft) = q.file_type() else { continue };
            if !ft.is_dir() { continue }
            let q_slug = q.file_name().to_string_lossy().to_string();
            out.insert(q_slug, ws_slug.clone());
        }
    }
    out
}

/// Walk a flat-list dir (`hooks/`, `rules/`) and invert each entry's
/// `queues` URL array into a queue_slug → [parent_slug, ...] map.
fn build_queue_back_refs(
    dir: PathBuf,
    lockfile: &Lockfile,
    field: &str,
) -> BTreeMap<String, Vec<String>> {
    let mut out: BTreeMap<String, Vec<String>> = BTreeMap::new();
    let Ok(entries) = std::fs::read_dir(&dir) else { return out };
    for e in entries.flatten() {
        let name = e.file_name().to_string_lossy().to_string();
        let Some(slug) = name.strip_suffix(".json") else { continue };
        if slug.ends_with(".remote") { continue }
        let Some(v) = read_json(&e.path()) else { continue };
        let queue_slugs = urls_to_slugs(&v, field, "queues", lockfile);
        for q in queue_slugs {
            out.entry(q.to_string()).or_default().push(slug.to_string());
        }
    }
    for v in out.values_mut() {
        v.sort();
    }
    out
}

/// Email template compound keys already encode the queue slug in their
/// middle segment (`<ws>/<q>/<t>`); inverting them needs no JSON reads.
fn build_queue_to_email_templates(lockfile: &Lockfile) -> BTreeMap<String, Vec<String>> {
    let mut out: BTreeMap<String, Vec<String>> = BTreeMap::new();
    if let Some(ets) = lockfile.objects.get("email_templates") {
        for key in ets.keys() {
            let mut parts = key.splitn(3, '/');
            if let (Some(_ws), Some(q), Some(_t)) = (parts.next(), parts.next(), parts.next()) {
                out.entry(q.to_string()).or_default().push(key.clone());
            }
        }
    }
    for v in out.values_mut() {
        v.sort();
    }
    out
}

fn build_engine_to_fields(paths: &Paths, lockfile: &Lockfile) -> BTreeMap<String, Vec<String>> {
    let mut out: BTreeMap<String, Vec<String>> = BTreeMap::new();
    let engines_dir = paths.engines_dir();
    let Ok(entries) = std::fs::read_dir(&engines_dir) else { return out };
    for e_entry in entries.flatten() {
        let Ok(ft) = e_entry.file_type() else { continue };
        if !ft.is_dir() { continue }
        let e_slug = e_entry.file_name().to_string_lossy().to_string();
        let fields_dir = paths.engine_fields_dir(&e_slug);
        let Ok(field_entries) = std::fs::read_dir(&fields_dir) else { continue };
        for f in field_entries.flatten() {
            let name = f.file_name().to_string_lossy().to_string();
            let Some(slug) = name.strip_suffix(".json") else { continue };
            if slug.ends_with(".remote") { continue }
            let Some(v) = read_json(&f.path()) else { continue };
            let Some(engine_url) = v.get("engine").and_then(|u| u.as_str()) else { continue };
            let Some(engine_slug) = lockfile.slug_for_url("engines", engine_url) else { continue };
            out.entry(engine_slug.to_string()).or_default().push(slug.to_string());
        }
    }
    for v in out.values_mut() {
        v.sort();
    }
    out
}

// ----- Top-level sections ---------------------------------------------

fn emit_inventory(md: &mut String, lockfile: &Lockfile) {
    md.push_str("## Inventory\n\n");
    let preferred = [
        "organization",
        "workspaces",
        "queues",
        "schemas",
        "inboxes",
        "hooks",
        "rules",
        "labels",
        "engines",
        "engine_fields",
        "workflows",
        "workflow_steps",
        "email_templates",
        "mdh_collections",
        "mdh_indexes",
    ];
    let mut emitted: std::collections::HashSet<&str> = std::collections::HashSet::new();
    for kind in preferred {
        if let Some(entries) = lockfile.objects.get(kind) {
            md.push_str(&format!("- {}: {}\n", kind, entries.len()));
            emitted.insert(kind);
        }
    }
    for (kind, entries) in lockfile.objects.iter() {
        if !emitted.contains(kind.as_str()) {
            md.push_str(&format!("- {}: {}\n", kind, entries.len()));
        }
    }
    md.push('\n');
}

fn emit_unknown_kinds(md: &mut String, lockfile: &Lockfile) {
    let mut extras: Vec<&String> = lockfile
        .objects
        .keys()
        .filter(|k| !RICH_KINDS.contains(&k.as_str()))
        .collect();
    extras.sort();
    for kind in extras {
        if let Some(entries) = lockfile.objects.get(kind) {
            if entries.is_empty() { continue }
            md.push_str(&format!("## {}\n\n", kind));
            for slug in entries.keys() {
                let entry = &entries[slug];
                if entry.id == 0 {
                    md.push_str(&format!("- `{}`\n", slug));
                } else {
                    md.push_str(&format!("- `{}` (id {})\n", slug, entry.id));
                }
            }
            md.push('\n');
        }
    }
}

// ----- Per-kind emitters ----------------------------------------------

fn emit_organization(md: &mut String, ctx: &IndexCtx<'_>) {
    let Some(entries) = ctx.lockfile.objects.get("organization") else { return };
    if entries.is_empty() { return }
    md.push_str("## organization\n\n");
    for (slug, entry) in entries.iter() {
        let path = ctx.paths.organization_file();
        let rel = "organization.json".to_string();
        let v = read_json(&path);
        write_header(md, slug, entry.id);
        write_name(md, v.as_ref());
        md.push_str(&format!("  - path: {rel}\n"));
    }
    md.push('\n');
}

fn emit_workspaces(md: &mut String, ctx: &IndexCtx<'_>) {
    let Some(entries) = ctx.lockfile.objects.get("workspaces") else { return };
    if entries.is_empty() { return }
    md.push_str("## workspaces\n\n");
    for (slug, entry) in entries.iter() {
        let path = ctx.paths.workspace_dir(slug).join("workspace.json");
        let v = read_json(&path);
        write_header(md, slug, entry.id);
        write_name(md, v.as_ref());
        md.push_str(&format!("  - path: workspaces/{slug}/workspace.json\n"));
        // queues field on workspace.json
        if let Some(v) = v.as_ref() {
            let qs = urls_to_slugs(v, "queues", "queues", ctx.lockfile);
            if !qs.is_empty() {
                md.push_str(&format!("  - queues: {}\n", join_slugs(&qs)));
            }
        }
    }
    md.push('\n');
}

fn emit_queues(md: &mut String, ctx: &IndexCtx<'_>) {
    let Some(entries) = ctx.lockfile.objects.get("queues") else { return };
    if entries.is_empty() { return }
    md.push_str("## queues\n\n");
    for (slug, entry) in entries.iter() {
        let Some(ws_slug) = ctx.queue_to_ws_slug.get(slug) else {
            // Queue exists in lockfile but not on disk; emit terse.
            write_header(md, slug, entry.id);
            continue;
        };
        let path = ctx.paths.queue_dir(ws_slug, slug).join("queue.json");
        let v = read_json(&path);
        write_header(md, slug, entry.id);
        write_name(md, v.as_ref());
        md.push_str(&format!(
            "  - path: workspaces/{ws_slug}/queues/{slug}/queue.json\n"
        ));
        md.push_str(&format!("  - workspace: `{ws_slug}`\n"));
        if let Some(v) = v.as_ref() {
            if let Some(s) = url_to_slug(v, "schema", "schemas", ctx.lockfile) {
                md.push_str(&format!("  - schema: `{s}`\n"));
            }
            if let Some(s) = url_to_slug(v, "inbox", "inboxes", ctx.lockfile) {
                md.push_str(&format!("  - inbox: `{s}`\n"));
            }
        }
        if let Some(hs) = ctx.queue_to_hooks.get(slug) {
            md.push_str(&format!("  - hooks: {}\n", join_slugs_owned(hs)));
        }
        if let Some(rs) = ctx.queue_to_rules.get(slug) {
            md.push_str(&format!("  - rules: {}\n", join_slugs_owned(rs)));
        }
        if let Some(ets) = ctx.queue_to_email_templates.get(slug) {
            md.push_str(&format!("  - email_templates: {}\n", join_slugs_owned(ets)));
        }
    }
    md.push('\n');
}

fn emit_schemas(md: &mut String, ctx: &IndexCtx<'_>) {
    let Some(entries) = ctx.lockfile.objects.get("schemas") else { return };
    if entries.is_empty() { return }
    md.push_str("## schemas\n\n");
    for (slug, entry) in entries.iter() {
        // Schema lives next to its queue (slug == queue slug by snapshot
        // convention). Skip terse if the queue isn't on disk.
        let Some(ws_slug) = ctx.queue_to_ws_slug.get(slug) else {
            write_header(md, slug, entry.id);
            continue;
        };
        let q_dir = ctx.paths.queue_dir(ws_slug, slug);
        let path = q_dir.join("schema.json");
        let v = read_json(&path);
        write_header(md, slug, entry.id);
        write_name(md, v.as_ref());
        md.push_str(&format!(
            "  - path: workspaces/{ws_slug}/queues/{slug}/schema.json\n"
        ));
        md.push_str(&format!("  - queue: `{slug}`\n"));
        let formulas = list_formula_fields(&q_dir);
        if !formulas.is_empty() {
            md.push_str(&format!(
                "  - formulas: {}\n",
                formulas
                    .iter()
                    .map(|f| format!("`{f}`"))
                    .collect::<Vec<_>>()
                    .join(", ")
            ));
        }
    }
    md.push('\n');
}

fn emit_inboxes(md: &mut String, ctx: &IndexCtx<'_>) {
    let Some(entries) = ctx.lockfile.objects.get("inboxes") else { return };
    if entries.is_empty() { return }
    md.push_str("## inboxes\n\n");
    for (slug, entry) in entries.iter() {
        let Some(ws_slug) = ctx.queue_to_ws_slug.get(slug) else {
            write_header(md, slug, entry.id);
            continue;
        };
        let path = ctx.paths.queue_dir(ws_slug, slug).join("inbox.json");
        let v = read_json(&path);
        write_header(md, slug, entry.id);
        write_name(md, v.as_ref());
        md.push_str(&format!(
            "  - path: workspaces/{ws_slug}/queues/{slug}/inbox.json\n"
        ));
        md.push_str(&format!("  - queue: `{slug}`\n"));
        if let Some(email) = v.as_ref().and_then(|v| v.get("email").and_then(|e| e.as_str())) {
            if !email.is_empty() {
                md.push_str(&format!("  - email: {email}\n"));
            }
        }
    }
    md.push('\n');
}

fn emit_hooks(md: &mut String, ctx: &IndexCtx<'_>) {
    let Some(entries) = ctx.lockfile.objects.get("hooks") else { return };
    if entries.is_empty() { return }
    md.push_str("## hooks\n\n");
    for (slug, entry) in entries.iter() {
        let path = ctx.paths.hooks_dir().join(format!("{slug}.json"));
        let py_path = ctx.paths.hooks_dir().join(format!("{slug}.py"));
        let v = read_json(&path);
        write_header(md, slug, entry.id);
        write_name(md, v.as_ref());
        if let Some(v) = v.as_ref() {
            let hook_type = v.get("type").and_then(|t| t.as_str());
            let events = v
                .get("events")
                .and_then(|e| e.as_array())
                .map(|a| a.iter().filter_map(|e| e.as_str()).collect::<Vec<_>>())
                .unwrap_or_default();
            match (hook_type, events.is_empty()) {
                (Some(t), false) => md.push_str(&format!(
                    "  - type: {t} · events: [{}]\n",
                    events.join(", ")
                )),
                (Some(t), true) => md.push_str(&format!("  - type: {t}\n")),
                (None, false) => {
                    md.push_str(&format!("  - events: [{}]\n", events.join(", ")))
                }
                _ => {}
            }
        }
        if py_path.exists() {
            md.push_str(&format!("  - path: hooks/{slug}.json (+ hooks/{slug}.py)\n"));
        } else {
            md.push_str(&format!("  - path: hooks/{slug}.json\n"));
        }
        if let Some(v) = v.as_ref() {
            let qs = urls_to_slugs(v, "queues", "queues", ctx.lockfile);
            if !qs.is_empty() {
                md.push_str(&format!("  - queues: {}\n", join_slugs(&qs)));
            }
        }
    }
    md.push('\n');
}

